#include "bits/stdc++.h"
using namespace std;

int main()
{
  int a = 10, b = 20;

  cout << "Sum = " << a + b << endl;
  cout << "Difference = " << a - b << endl;
  cout << "Multiplication = " << a * b << endl;
  cout << "Divide = " << b / a << endl;
  cout << "Modulo = " << b % a << endl; // Remainder
  return 0;
}